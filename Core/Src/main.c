  set_leds(0, font[c * 5]);
  set_leds(1, font[c * 5 + 1]);
  set_leds(2, font[c * 5 + 2]);
  set_leds(3, font[c * 5 + 3]);
  set_leds(4, font[c * 5 + 4]);
